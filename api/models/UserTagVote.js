module.exports = {

  attributes: {

    //  ╔═╗╦═╗╦╔╦╗╦╔╦╗╦╦  ╦╔═╗╔═╗
    //  ╠═╝╠╦╝║║║║║ ║ ║╚╗╔╝║╣ ╚═╗
    //  ╩  ╩╚═╩╩ ╩╩ ╩ ╩ ╚╝ ╚═╝╚═╝
    vote: {
      type: "number",
      defaultsTo: 1
    },


    //  ╔═╗╔╦╗╔╗ ╔═╗╔╦╗╔═╗
    //  ║╣ ║║║╠╩╗║╣  ║║╚═╗
    //  ╚═╝╩ ╩╚═╝╚═╝═╩╝╚═╝


    //  ╔═╗╔═╗╔═╗╔═╗╔═╗╦╔═╗╔╦╗╦╔═╗╔╗╔╔═╗
    //  ╠═╣╚═╗╚═╗║ ║║  ║╠═╣ ║ ║║ ║║║║╚═╗
    //  ╩ ╩╚═╝╚═╝╚═╝╚═╝╩╩ ╩ ╩ ╩╚═╝╝╚╝╚═╝
    sermon: {
      model: "sermon"
    },

    tag: {
      model: "tag"
    },

    user: {
      model: "user"
    },

  },

  /* Get last three with highest vote */
  get: async (sermonId, currentUser) => {
    var tags = await UserTagVote.find({ where: { sermon: sermonId, user: currentUser }, select: ['id'] })
      .sort('vote DESC')
      .limit(3)
      .populate('tag');

    tags.forEach(element => {
      element.id = element.tag.id
      element.name = element.tag.name
      delete element.tag
    })

    return tags;
  },

};

